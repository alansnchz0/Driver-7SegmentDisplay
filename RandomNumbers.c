/*!
    \fn  randomSeed.
    \param	seed.
    \return  void
    \brief  Sets the seed value for random().
*/
void randomSeed(unsigned long seed)
{
  if (seed != 0) {
    srandom(seed);
  }
}
/*!
    \fn  pinMode.
    \param	max.
    \return  void
    \brief  Generates random numbers.
*/
long random(long max)
{
  if (max == 0) {
    return 0;
  }
  return random() % max;
}
